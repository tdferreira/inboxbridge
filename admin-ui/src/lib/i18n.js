export const languageOptions = [
  'en',
  'fr',
  'de',
  'pt-PT',
  'pt-BR',
  'es'
]

export function normalizeLocale(locale) {
  if (!locale) return 'en'
  const exact = languageOptions.find((candidate) => candidate.toLowerCase() === String(locale).toLowerCase())
  if (exact) return exact
  if (String(locale).toLowerCase().startsWith('pt-br')) return 'pt-BR'
  if (String(locale).toLowerCase().startsWith('pt')) return 'pt-PT'
  if (String(locale).toLowerCase().startsWith('fr')) return 'fr'
  if (String(locale).toLowerCase().startsWith('de')) return 'de'
  if (String(locale).toLowerCase().startsWith('es')) return 'es'
  return 'en'
}

function interpolate(template, params = {}) {
  return template.replace(/\{(\w+)\}/g, (_, key) => params[key] ?? `{${key}}`)
}

export function translate(locale, key, params) {
  const normalized = normalizeLocale(locale)
  const value = simpleTranslations[normalized]?.[key] || english[key] || key
  return interpolate(value, params)
}
