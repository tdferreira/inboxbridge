export const translationCatalog = {
  en: english,
  fr: french,
  de: german,
  'pt-PT': portuguesePortugal,
  'pt-BR': portugueseBrazil,
  es: spanish
}

export function normalizeLocale(value) {
  const input = String(value || 'en').trim()
  if (translationCatalog[input]) {
    return input
  }
  const lower = input.toLowerCase()
  if (lower.startsWith('pt-br')) {
    return 'pt-BR'
  }
  if (lower.startsWith('pt')) {
    return 'pt-PT'
  }
  if (lower.startsWith('fr')) {
    return 'fr'
  }
  if (lower.startsWith('de')) {
    return 'de'
  }
  if (lower.startsWith('es')) {
    return 'es'
  }
  return 'en'
}

export function languageFlag(locale) {
  return LANGUAGE_FLAGS[locale] || '🌐'
}

export function translate(locale, key, params = {}) {
  const normalized = normalizeLocale(locale)
  const dictionary = translationCatalog[normalized] || translationCatalog.en
  const template = dictionary[key] || translationCatalog.en[key] || key
  return template.replace(/\{(\w+)\}/g, (_, name) => params[name] ?? `{${name}}`)
}
